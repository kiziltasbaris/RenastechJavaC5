package Day2;

public class Variables {

    public static void main(String[] args) {

        // Comments doesnt have any funclity
        //There are 2 type of comments first one single line second one is multi line comment
        //comments wont effect your actual code

        System.out.println("First Name: Baris");
        System.out.println("Last Name: Kiziltas");
        System.out.println("Company name: Oracle");
        System.out.println("Salary: 120.000K");

        //escape sequences:
        // \t: paragraph space
        // \n: new Line
        // \" prints double quote" on the console
        // \\: prints \ on the console
        // \' : prints single quote
        // ': prints single quote

        System.out.println("Renastech");

        // \t: paragraph space
        System.out.println(" \t Renastech");
        System.out.println("\t\t\t\t\t Renastech");

        // \n: new Line

        System.out.println("Hello World");
        System.out.println("Hello \n World");
        System.out.println("Hello \n\t World \t Java");

        // \\:prints \ on the console

        System.out.println("\\\\");
        System.out.println("//");

        // \" : print double quote " on the console
        System.out.println("Today is \" Tuesday \" . ");
        System.out.println("Today is 'Friday'. ");

        //Operators



        byte oscarAge=32;
        byte libbyAge=30;

        double omerAge=31.5;
        double rickAge=20.3;

        //Plus

        System.out.println(oscarAge+libbyAge);
        System.out.println(libbyAge+rickAge);

        //Minus operator

        System.out.println(oscarAge-libbyAge);
        System.out.println(omerAge-rickAge);

        //Multiple Operator

        System.out.println(oscarAge*2);
        System.out.println(rickAge*omerAge);

        //division

        System.out.println(libbyAge/2);
        System.out.println(oscarAge/3);

        //reminder

        System.out.println(10%5);
        System.out.println(10%3);
        System.out.println(10/2);









    }
}
